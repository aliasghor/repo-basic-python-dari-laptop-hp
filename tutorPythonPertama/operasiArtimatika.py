x = 100;
y = 10;

#tambah
hasil = x + y;
print(x, "+", y, "=", hasil);

#kurang
hasil = x - y;
print(x, "-", y, "=", hasil);

#kali
hasil = x * y;
print(x, "x", y, "=", hasil);

#bagi
hasil = x / y;
print(x, ":", y, "=", hasil);

# pangkat
hasil = x ** y;
print(x, "pangkat", y, "=", hasil);

#modulus
hasil = x % y;
print(x, "modulus", y, "=", hasil);

# floor division
hasil = x // y;
print(x, "floor division", y, "=", hasil);

#prioritas operasi
a = 3;
b = 2;
c = 4;

hasil = (a + b) * c / b - a;

print(hasil);